package com.example.dell.quanly.Adapter;

public class Xuly  {
    private int id;
    private String Ten;

    public Xuly(int id, String ten) {
        this.id = id;
        Ten = ten;
    }

    public Xuly() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTen() {
        return Ten;
    }

    public void setTen(String ten) {
        Ten = ten;
    }
}
